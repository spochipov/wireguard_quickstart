#!/bin/bash

# Скрипт для автоматической установки и настройки OpenVPN сервера на Debian 12
# с учетом максимальной производительности и обхода блокировок.

# ВАЖНО:
# 1. Этот скрипт предполагает чистый VPS на Debian 12.
# 2. Он использует популярный и хорошо поддерживаемый скрипт от angristan (github.com/angristan/openvpn-install).
#    Это минимизирует ручную конфигурацию и включает множество оптимизаций и функций безопасности.
# 3. После запуска скрипта от angristan, вам потребуется ввести некоторые параметры.
#    Рекомендации будут даны в этом скрипте, чтобы помочь вам сделать оптимальный выбор.

# --- Начальная проверка и подготовка ---

# Проверка, что скрипт запускается от root
if [[ $EUID -ne 0 ]]; then
   echo "Этот скрипт должен быть запущен от root пользователя."
   exit 1
fi

# Проверка операционной системы на Debian 12
if ! grep -q "Debian GNU/Linux 12" /etc/os-release; then
    echo "Этот скрипт предназначен только для Debian 12 (Bookworm)."
    echo "Ваша ОС:"
    cat /etc/os-release
    exit 1
fi

echo "--- Начинаем установку OpenVPN сервера на Debian 12 ---"
echo ""

# Обновление списка пакетов и установка последних обновлений системы
echo "Обновление системных пакетов..."
apt update -y
apt upgrade -y
apt autoremove -y
apt clean
echo "Система обновлена."

# Установка необходимых утилит: curl для загрузки скрипта
echo "Установка curl (если отсутствует)..."
apt install -y curl
echo "Установка ufw (если отсутствует)..."
apt install -y ufw
echo "Необходимые утилиты установлены."

# --- Загрузка и подготовка скрипта OpenVPN установки ---

# Удаляем старую версию (если есть) и всегда загружаем последнюю.
# Это гарантирует, что мы используем актуальную, неповрежденную версию,
# что может помочь избежать ошибок типа "command not found", если файл был поврежден.
INSTALL_SCRIPT_URL="https://raw.githubusercontent.com/angristan/openvpn-install/master/openvpn-install.sh"
INSTALL_SCRIPT_PATH="/tmp/openvpn-install.sh" # Измененный путь для временных скриптов

echo "Удаление старой версии скрипта angristan/openvpn-install.sh (если она есть)..."
rm -f "$INSTALL_SCRIPT_PATH"

echo "Загрузка последней версии скрипта angristan/openvpn-install.sh с $INSTALL_SCRIPT_URL..."
if ! curl -o "$INSTALL_SCRIPT_PATH" "$INSTALL_SCRIPT_URL"; then
    echo "ОШИБКА: Не удалось загрузить скрипт OpenVPN установки."
    echo "Проверьте подключение к интернету или корректность URL: $INSTALL_SCRIPT_URL"
    exit 1
fi
chmod +x "$INSTALL_SCRIPT_PATH"
echo "Скрипт angristan/openvpn-install.sh успешно загружен и сделан исполняемым."
echo ""

# --- Запуск интерактивной установки OpenVPN ---

echo "--- ВНИМАНИЕ: Запуск интерактивного скрипта OpenVPN установки ---"
echo "Пожалуйста, внимательно следуйте инструкциям на экране, которые появятся сейчас."
echo "Ниже приведены РЕКОМЕНДАЦИИ для выбора параметров, чтобы достичь максимальной производительности"
echo "и обхода возможных блокировок. Запомните их или держите под рукой."
echo ""
echo "РЕКОМЕНДАЦИИ ПО ВЫБОРУ ОПЦИЙ СКРИПТА ANGRISTAN:"
echo "---------------------------------------------------"
echo "1.  IP Address: Обычно скрипт сам определяет правильный публичный IP. Просто подтвердите его."
echo "2.  Protocol: "
echo "    - Для МАКСИМАЛЬНОЙ ПРОИЗВОДИТЕЛЬНОСТИ и если нет проблем с блокировками: выберите [1] UDP."
echo "    - Для ОБХОДА БЛОКИРОВОК (если UDP блокируется или вы хотите маскировку под HTTPS): выберите [2] TCP."
echo "3.  Port: "
echo "    - Если вы выбрали UDP: оставьте стандартный (1194) или выберите [4] Custom и любой другой нестандартный порт (например, 2048, 50000). Это может помочь, если 1194 блокируется."
echo "    - Если вы выбрали TCP для обхода блокировок: выберите [4] Custom и ВВЕДИТЕ 443. Это замаскирует OpenVPN трафик под HTTPS."
echo "4.  DNS servers: Выберите надежные публичные DNS, которые не блокируются."
echo "    - Для обхода DNS-блокировок: [4] Cloudflare (1.1.1.1) (НАСТОЯТЕЛЬНО РЕКОМЕНДУЕТСЯ), [3] Google, [5] AdGuard. Это поможет, если ваш провайдер блокирует DNS-запросы."
echo "5.  Custom name for the first client: Введите любое имя для вашего первого клиентского файла (например, 'myclient' или 'phone')."
echo "6.  Cipher: Выберите современный и производительный шифр: [2] AES-256-GCM (РЕКОМЕНДУЕТСЯ). Это быстрый и безопасный шифр."
echo "7.  Do you want to enable TLS-Crypt? (recommended): Выберите [1] Yes. Это КРАЙНЕ ВАЖНО для обфускации трафика и повышения безопасности, так как оно шифрует управляющий канал OpenVPN."
echo "8.  Enable client-to-client communication?: Выберите [2] No, если вашим OpenVPN клиентам не нужно общаться между собой. Это усиливает изоляцию."
echo "9.  Do you want to enable redirect-gateway (all traffic to the VPN)?: Выберите [1] Yes. Это перенаправит весь ваш интернет-трафик через VPN."
echo "10. Do you want to enable EDNS0, for a better compatibility with public DNS servers?: Выберите [1] Yes. Это улучшает совместимость с публичными DNS."
echo "11. Do you want to compress the traffic? (useful when you have a slow connection): "
echo "    - Для МАКСИМАЛЬНОЙ ПРОИЗВОДИТЕЛЬНОСТИ на быстрых соединениях: выберите [2] No (None). Компрессия может снизить скорость из-за нагрузки на CPU."
echo "    - Если нужна легкая компрессия с хорошей производительностью: [1] LZ4."
echo ""
echo "!!! ДОЖДИТЕСЬ ЗАВЕРШЕНИЯ СКРИПТА. ОН СООБЩИТ ВАМ ИМЯ СОЗДАННОГО .OVPN ФАЙЛА !!!"
echo ""

# Запуск основного скрипта установки OpenVPN
bash "$INSTALL_SCRIPT_PATH"

echo ""
echo "--- Первичная настройка OpenVPN завершена ---"
echo "Скрипт angristan/openvpn-install.sh только что успешно завершил работу."
echo "Он должен был сообщить вам имя созданного файла конфигурации клиента (.ovpn)."
echo "Обычно этот файл находится в домашней директории пользователя, от имени которого запускался скрипт (скорее всего, /root/)."
echo "Например: /root/ВАШЕ_ИМЯ_КЛИЕНТА.ovpn"
echo "ОБЯЗАТЕЛЬНО СКОПИРУЙТЕ ЭТОТ ФАЙЛ НА ВАШЕ КЛИЕНТСКОЕ УСТРОЙСТВО!"
echo ""

# --- Применение дополнительных оптимизаций sysctl для производительности сети ---
echo "--- Применение дополнительных оптимизаций sysctl для производительности сети ---"
# Эти настройки улучшают общую производительность сетевого стека Linux, что положительно сказывается на OpenVPN.
SYSCTL_CONF="/etc/sysctl.d/99-openvpn-performance.conf"

cat <<EOF > "$SYSCTL_CONF"
# Увеличение лимитов файловых дескрипторов
fs.file-max = 100000

# Увеличение размеров буферов TCP для лучшей производительности на высокоскоростных сетях
# Это помогает поглощать большие объемы данных без потерь на быстрых соединениях
net.core.rmem_max = 67108864
net.core.wmem_max = 67108864
net.core.rmem_default = 65536
net.core.wmem_default = 65536
net.core.netdev_max_backlog = 4000
net.core.somaxconn = 65535 # Увеличение очереди соединений для лучшей обработки большого числа новых соединений

# Оптимизации TCP
net.ipv4.tcp_rmem = 4096 87380 67108864 # Минимальный, по умолчанию, максимальный размер буфера чтения TCP
net.ipv4.tcp_wmem = 4096 65536 67108864 # Минимальный, по умолчанию, максимальный размер буфера записи TCP
net.ipv4.tcp_congestion_control = bbr # Использование BBR в качестве алгоритма контроля перегрузок (разработан Google, очень эффективен для высокоскоростных сетей)
net.ipv4.tcp_max_syn_backlog = 8192 # Увеличение очереди SYN-запросов
net.ipv4.tcp_tw_reuse = 1 # Разрешает повторное использование TIME_WAIT сокетов для быстрой утилизации портов
net.ipv4.tcp_fastopen = 3 # Включает TCP Fast Open (ускоряет установление TCP соединений)
net.ipv4.tcp_fin_timeout = 30 # Уменьшение времени ожидания FIN-ACK
net.ipv4.tcp_keepalive_time = 600 # Время, после которого начинают посылаться Keepalive-пакеты (10 минут)
net.ipv4.tcp_keepalive_probes = 5 # Количество Keepalive-пакетов перед отключением
net.ipv4.tcp_keepalive_intvl = 30 # Интервал между Keepalive-пакетами
net.ipv4.ip_local_port_range = 1024 65000 # Расширение диапазона локальных портов для исходящих соединений
EOF

echo "Добавляем настройки производительности в $SYSCTL_CONF..."
sysctl --system # Применяем новые настройки sysctl

echo "Оптимизации sysctl применены и будут сохранены при перезагрузке."
echo ""

echo "--- Управление клиентами (добавление/удаление) ---"
echo "Чтобы добавить нового VPN клиента или удалить существующего, просто запустите скрипт OpenVPN установки снова:"
echo " bash $INSTALL_SCRIPT_PATH"
echo ""
echo "При повторном запуске скрипт angristan/openvpn-install.sh автоматически обнаружит существующую установку OpenVPN и предложит вам следующие опции:"
echo "1. Add a new user          (Добавить нового клиента)"
echo "2. Revoke an existing user (Отозвать существующего клиента)"
echo "3. Remove OpenVPN          (Полностью удалить OpenVPN сервер)"
echo "4. Exit                    (Выйти)"
echo "Выберите '1' для добавления нового клиента и следуйте его инструкциям для создания нового .ovpn файла."

echo ""
echo "--- Важные замечания после установки ---"
echo "1.  **Фаервол провайдера:** Убедитесь, что порт, который вы выбрали для OpenVPN (например, 1194 UDP или 443 TCP), открыт в панели управления вашего VPS-провайдера (если у него есть свой фаервол, помимо фаервола ОС)."
echo "    Скрипт `angristan/openvpn-install` автоматически настраивает правила `ufw` или `iptables` в ОС сервера."
echo "2.  **Обновления:** Регулярно обновляйте вашу систему для безопасности: ` apt update &&  apt upgrade`."
echo "3.  **Мониторинг:** Для просмотра логов OpenVPN сервера используйте команду: ` journalctl -u openvpn-server@server -f` (для систем с `systemd`)."
echo "4.  **Копирование .ovpn файла:** После создания .ovpn файла, вам нужно будет скачать его с VPS на ваше клиентское устройство. Используйте для этого утилиты типа `scp` или `sftp` (например, WinSCP для Windows, Cyberduck для Mac)."
echo "    Пример команды `scp` с вашего локального компьютера:"
echo "    `scp root@ВАШ_IP_VPS:/root/ИМЯ_КЛИЕНТА.ovpn ~/MyVPNConfigs/`"
echo "    Замените `ВАШ_IP_VPS` на IP вашего сервера, `ИМЯ_КЛИЕНТА.ovpn` на фактическое имя файла."
echo "    После этого импортируйте его в официальное приложение OpenVPN Connect или другое совместимое клиентское приложение."
echo ""
echo "Установка и настройка OpenVPN завершена успешно!"
echo "Наслаждайтесь вашим приватным и производительным VPN!"
echo ""
